package org.obolibrary.oboformat.parser;

public class OBOFormatDanglingReferenceException extends OBOFormatException {

	public OBOFormatDanglingReferenceException() {
	}

	public OBOFormatDanglingReferenceException(String arg0) {
		super(arg0);
	}

	public OBOFormatDanglingReferenceException(Throwable arg0) {
		super(arg0);
	}

	public OBOFormatDanglingReferenceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
